//                   JSON
// JSON stands for javaScript Object notation - its a light weight format for storing and transferring data between client and a server.
// JSON (javaScript Object notation) is the universal language of data interchange in modern computing.
// its lightweight, human readable, and machine parsable.
//every major programming language (python,java,Go,Rust,etc.) can parse and generate JSON easily.
//universal format(used in 99% APIs)
// no need for exrtra encoding/decoding layers
// it looks like javascript objects but is actually a text format.


// property                              |           explanation
// blanguage independent                 |   works in all languages (js,pyhton,java,Go,etc)
// lightweight                           |   faster than XML or YAML
// flexible                              |   supports nested structures, arrays, objects
// standardized                          |   defined by ECMA-404
// secure                                |   no code execution(unlike XML parsers)
// extensible                            |   can be used for config, APIs, logs, data storage




