//         Assignments operators


// |  operators  |  Example  |  same as       |
// |  --------   | --------  | -------------  |
// |     =       |  x = 5    |   x = 5        |
// |     +=      |  x += 5   |   x = x + 5    |
// |     -=      |  x -= 5   |   x = x - 5    | 
// |     *=      |  x *= 5   |   x = x * 5    | 
// |     /=      |  x /= 5   |   x = x / 5    | 
// |     %=      |  x %= 5   |   x = x % 5    |
// |     **=     |  x **= 3  |   x = x ** 3   |


let box = 5
box += 5
console.log(box)
box -= 5
console.log(box)
box *= 5
console.log(box)
box /= 5
console.log(box)
box %= 5
console.log(box)
box **= 5
console.log(box)

